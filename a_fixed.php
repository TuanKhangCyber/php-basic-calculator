<?php
$x = 7;          // Gán giá trị 7 cho biến $x
$y = 3;          // Gán giá trị 3 cho biến $y

echo "x = $x, y = $y<br>";     // In ra màn hình giá trị của $x và $y
echo "Cộng: " . ($x + $y) . "<br>";   // Phép cộng
echo "Trừ: " . ($x - $y) . "<br>";    // Phép trừ
echo "Nhân: " . ($x * $y) . "<br>";   // Phép nhân
echo "Chia: " . ($x / $y) . "<br>";   // Phép chia (số thực)
echo "Chia lấy dư: " . ($x % $y) . "<br>";  // Phép chia lấy dư (modulo)
?>
